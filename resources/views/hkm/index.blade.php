    document.addEventListener("DOMContentLoaded", function () {
      const smoothScrollLinks = document.querySelectorAll(".smooth-scroll-link");

      // Função para lidar com a rolagem suave
      function smoothScroll(event) {
        event.preventDefault();
        const targetId = event.target.getAttribute("data-target");
        const targetOffset = event.target.getAttribute("data-offset");
        const targetSection = document.querySelector(targetId);

        if (targetSection) {
          const targetPosition = targetSection.getBoundingClientRect().top + window.scrollY - targetOffset;
          window.scrollTo({
            top: targetPosition,
            behavior: "smooth",
          });
        }
      }

      // Adicione um ouvinte de eventos para cada link de ancoragem
      smoothScrollLinks.forEach(function (link) {
        link.addEventListener("click", smoothScroll);
      });
    });
